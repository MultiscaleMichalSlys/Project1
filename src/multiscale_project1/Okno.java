    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int pr=Integer.parseInt(promien_zarodki.getText());
        Random r = null;
        int x=0,y=0;
        int id=1;
        rozrost.wyczysc();
        boolean ok=false;
        
        for(int i=0; i<Integer.parseInt(jTextField1.getText()); i++)
        {
            ok = false;
            while(!ok)
            {
                r=new Random();
                x=r.nextInt(400);
                y=r.nextInt(400);
                ok = true;
                for(int a=-pr; a<pr*2;a++)
                {
                    for(int b=-pr; b<pr*2;b++)
                    {
                        if (sqrt((a-x)^2+(b-y)^2) > pr) ok=false;
                        else if ((x+a)<0 || (y+b)<0 || (x+a)>399 || (y+b)>399) ok=false;
                        else if (rozrost.tab[x+a][y+b] > 0)ok=false;
                    }
                }
            }
            if (ok) {
                rozrost.dodajZarodek(x, y, id++);
            }
            System.out.println(x + " " + y);

        }
        rysuj();


    }//GEN-LAST:event_jButton5ActionPerformed

