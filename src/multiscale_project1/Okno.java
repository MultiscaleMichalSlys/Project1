    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        Boolean bc = false;
        if (this.bc1.isSelected()) {
            bc = true;//periodyczne
        } else if (this.bc2.isSelected()) {
            bc = false;  //nieperiodyczne
        }
        
        int jakie = 0;
        if (this.otocz_moora.isSelected()) jakie = 0;
            else if (this.otocz_neumana.isSelected()) jakie = 1;
            else if (this.otocz_hexp.isSelected()) jakie = 2;
            else if (this.otocz_hexl.isSelected()) jakie = 3;
            else if (this.otocz_hexlos.isSelected()) jakie = 4;
            else if (this.otocz_penta.isSelected()) jakie = 5;
            else if (this.los2.isSelected()) jakie = 6;
            else if (this.otocz_los.isSelected()) jakie = 7;
        
        rekrystal = new Rekrystalizacja(this.rozrost, Integer.parseInt(jTextField1.getText()));
        rekrystal.znajdz_granice();

        Graphics g = jPanel1.getGraphics();
        g.clearRect(0, 0, 400, 400);
        for (int i = 0; i < 400; i++) {
            for (int j = 0; j < 400; j++) {
                if (rozrost.tab[i][j] > 0) {
                    g.setColor(Color.getHSBColor(rozrost.tab[i][j] * 20 % 255 + 1, rozrost.tab[i][j] % 255 * 50 + 1, rozrost.tab[i][j] % 255 * 37 + 1));
                    g.fillRect(i, j, 1, 1);
                } else {
                    g.setColor(Color.black);
                    g.fillRect(i, j, 1, 1);
                }
                if (rekrystal.granice[i][j]) {
                    g.setColor(Color.black);
                    g.fillRect(i, j, 1, 1);
                }

            }
        }

        try {
            Thread.sleep(200);
        } catch (InterruptedException ex) {
            Logger.getLogger(Okno.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        rekrystal.koniec=false;
        int odl=Integer.parseInt(jTextField2.getText());
        int ile=500000;
       
        while (!rekrystal.koniec) {
            

            rekrystal.rekrystalizuj(bc, jakie, odl, ile);

            

           // g = jPanel1.getGraphics();
           // g.clearRect(0, 0, 400, 400);
            for (int i = 0; i < 400; i++) {
                for (int j = 0; j < 400; j++) {
                    if (rekrystal.zrek.tab[i][j] >0) {
                        g.setColor(Color.getHSBColor(rekrystal.zrek.tab[i][j] * 3 % 255 + 1, rekrystal.zrek.tab[i][j] % 255 * 5 + 1, rekrystal.zrek.tab[i][j] % 255 * 7 + 1));
                        g.fillRect(i, j, 1, 1);

                    }
                }
            }
            
        }
       // rozrost.koniec = false;

    }//GEN-LAST:event_jButton6ActionPerformed

