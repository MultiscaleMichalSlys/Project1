    public void rysuj()
    {
        Graphics g = jPanel1.getGraphics();
        g.clearRect(0,0,400,400);
        for(int i=0; i<400; i++)
        {
           for(int j=0; j<400; j++)
           {
               if (rozrost.tab[i][j] >0)
               {
                   g.setColor(Color.getHSBColor(rozrost.tab[i][j]*20%255+1,rozrost.tab[i][j]%255*50+1,rozrost.tab[i][j]%255*37+1));
                   g.fillRect(i,j,1,1);
               }
               else
               {
                   g.setColor(Color.black);
                   g.fillRect(i,j,1,1);
               }
               
           }
        }
    }
    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Boolean bc=false;
        if (this.bc1.isSelected())  bc=true;//periodyczne
        else if (this.bc2.isSelected()) bc=false;  //nieperiodyczne
        
        while(!rozrost.koniec) {
        if (this.otocz_moora.isSelected())rozrost.moor(bc,0); 
        else if (this.otocz_neumana.isSelected()) rozrost.neuman(bc);
        else if (this.otocz_hexp.isSelected()) rozrost.hexagp(bc);
        else if (this.otocz_hexl.isSelected()) rozrost.hexagl(bc);
        else if (this.otocz_hexlos.isSelected()) rozrost.hexaglos(bc);
        else if (this.otocz_penta.isSelected()) rozrost.penta(bc);
        else if (this.los2.isSelected()) rozrost.los2(bc);
        else if (this.otocz_los.isSelected()) rozrost.los(bc, Integer.parseInt(promien.getText()));
        
       
        
        rysuj();
        }
        rozrost.koniec=false;
        
    }//GEN-LAST:event_jButton1ActionPerformed

